    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        imc.getPessoa().setNome(inputNome.getText());
        imc.getPessoa().setSexo(converteSexo(inputSexo.getSelectedIndex()));
        try {
            imc.getPessoa().setAltura(Float.parseFloat(inputAltura.getText()));
            imc.getPessoa().setPeso(Float.parseFloat(inputPeso.getText()));
        } catch (Exception ex) {
            Msg.validaNegativo();
        }
        
        String resultado = "";
        resultado += "Nome: "+imc.getPessoa().getNome().toUpperCase();
        resultado += "\nSexo: "+imc.getPessoa().getSexo();
        resultado += "\nAltura: "+imc.getPessoa().getAltura()+" Metros";
        resultado += "\nPeso: "+imc.getPessoa().getPeso()+" Kilogramas";
        resultado += "\nIMC: "+imc.calculeIMC();
        
        labelSaida.setText(resultado);
        
    }//GEN-LAST:event_btnEnviarActionPerformed

    private String converteSexo(Integer codigo){
        switch (codigo){
            case 0:
                return "Masculino";
            case 1:
                return "Feminino";
            default:
                return null;
        }
    }
